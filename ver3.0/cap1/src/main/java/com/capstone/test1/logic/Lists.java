package com.capstone.test1.logic;

public class Lists {
	private int num;
	private String name;
	private String price;
	private String type;
	private String brand;
	private String image;
	private String color;
	private String detail;
	private String modeling;
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public String getDetail() {
		return detail;
	}
	public void setDetail(String detail) {
		this.detail = detail;
	}
	public String getModeling() {
		return modeling;
	}
	public void setModeling(String modeling) {
		this.modeling = modeling;
	}
	@Override
	public String toString() {
		return "Lists [num=" + num + ", name=" + name + ", price=" + price + ", type=" + type + ", brand=" + brand
				+ ", image=" + image + ", color=" + color + ", detail=" + detail + ", modeling=" + modeling + "]";
	}
	
	
	
	
	

}
