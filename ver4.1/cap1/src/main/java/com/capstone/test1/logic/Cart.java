package com.capstone.test1.logic;

public class Cart {
	private String id;
	private int num;
	private int count;
	private String name;
	private String brand;
	private int price;
	private String image;
	private String detail;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public String getDetail() {
		return detail;
	}
	public void setDetail(String detail) {
		this.detail = detail;
	}
	@Override
	public String toString() {
		return "Cart [id=" + id + ", num=" + num + ", count=" + count + ", name=" + name + ", brand=" + brand
				+ ", price=" + price + ", image=" + image + ", detail=" + detail + "]";
	}
	
	
	
	
}
